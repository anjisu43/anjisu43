
public class Festival0906_03 {

	public static void main(String[] args) {

		for(int space=5,star=1;space>=1;space--,star++) {
			
			for(int a = 0; a < space ; a++) {
				System.out.print(" ");
			}
			for(int a= 0 ; a < star ; a++) {
				System.out.print("*");
			}
			System.out.println();}
		

	}

}
