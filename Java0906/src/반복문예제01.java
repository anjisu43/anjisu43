
public class 반복문예제01 {

	public static void main(String[] args) {
		//1. for문을 활용해서 21에서 57까지 숫자 중 홀수만 출력하기
		//(출력 결과가 가로로 나오기)
		
		for(int num1=21;num1<=57;num1+=2){
			System.out.print(num1+" ");
		}
		System.out.println( );
		//2. for문을 활용해서 96에서 53까지 출력하기
		for(int num2=96;num2>=53;num2--) {
			System.out.print(num2+" ");
		}

	}

}
 